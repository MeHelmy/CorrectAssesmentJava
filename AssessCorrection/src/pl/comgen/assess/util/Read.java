package pl.comgen.assess.util;

/**
 * @author medhat
 *
 */
public class Read {
	private String seqName, seqValue;
	private long seqLength;
	/**
	 * @return the seqName
	 */
	public String getSeqName() {
		return seqName;
	}
	/**
	 * @param seqName the seqName to set
	 */
	public void setSeqName(String seqName) {
		this.seqName = seqName;
	}
	/**
	 * @return the seqValue
	 */
	public String getSeqValue() {
		return seqValue;
	}
	/**
	 * @param seqValue the seqValue to set
	 */
	public void setSeqValue(String seqValue) {
		this.seqValue = seqValue;
	}
	/**
	 * @return the seqLength
	 */
	public long getSeqLength() {
		return seqLength;
	}
	/**
	 * @param seqLength the seqLength to set
	 */
	public void setSeqLength(long seqLength) {
		this.seqLength = seqLength;
	}

}
